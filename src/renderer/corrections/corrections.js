    function syncVisibility() {
        const isActive = currentView() === 'corrections';
        elements.view.hidden = !isActive;
        elements.button.classList.toggle('active', isActive);
        elements.button.setAttribute('aria-pressed', String(isActive));
        if (isActive && !loaded) {
            loadCorrections();
        }
        if (isActive) {
            void showCorrectionsDisclaimerIfNeeded();
        }
    }

    async function loadCorrections(force = false) {
        if (loading) return;
        loading = true;
        elements.refresh.disabled = true;
        elements.refresh.classList.toggle('loading', force);
        elements.refreshLabel.textContent = tr(force ? 'corrections_refreshing' : 'corrections_refresh');

        if (!loaded) {
            elements.loading.hidden = false;
            elements.empty.hidden = true;
            elements.noResults.hidden = true;
            elements.gridWrap.hidden = true;
            elements.footer.hidden = true;
        }

        try {
            const request = force ? api.refresh() : api.list();
            const minimumLoadingTime = force
                ? new Promise(resolve => setTimeout(resolve, 500))
                : Promise.resolve();
            const [result] = await Promise.all([request, minimumLoadingTime]);

            if (!result.success) {
                notify(tr(force ? 'corrections_error_refresh_failed' : 'corrections_error_load'), 'error');
                elements.loading.hidden = true;
                return;
            }

            items = result.items;
            loaded = true;
            currentPage = 1;
            render();

            if (result.stale) {
                notify(tr('corrections_refresh_stale'));
            }
        } catch (_) {
            notify(tr(force ? 'corrections_error_refresh_failed' : 'corrections_error_load'), 'error');
            elements.loading.hidden = true;
        } finally {
            loading = false;
            elements.refresh.disabled = false;
            elements.refresh.classList.remove('loading');
            elements.refreshLabel.textContent = tr('corrections_refresh');
            render();
        }
    }

