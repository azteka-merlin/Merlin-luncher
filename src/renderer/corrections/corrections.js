        elements.progressSpeedLabel.textContent = tr('corrections_progress_speed');
        elements.progressSpeed.textContent = '—';
        elements.progressRemainingLabel.textContent = tr('corrections_progress_remaining');
        elements.progressRemaining.textContent = '—';
