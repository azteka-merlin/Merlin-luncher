    function filteredItems() {
        const term = String(elements.search.value || '').trim().toLocaleLowerCase();
        if (!term) return [...items];
        return items.filter(item =>
            item.gameName.toLocaleLowerCase().includes(term)
            || item.appId.includes(term));
    }

    function paginate(collection, requestedPage) {
        const totalPages = Math.max(1, Math.ceil(collection.length / pageSize));
        const page = Math.max(1, Math.min(requestedPage, totalPages));
        const start = (page - 1) * pageSize;
        return {
            currentPage: page,
            totalPages,
            visible: collection.slice(start, start + pageSize)
        };
    }

    function pageSequence(totalPages) {
        if (totalPages <= 7) {
            return Array.from({ length: totalPages }, (_, index) => index + 1);
        }

        const pages = new Set([1, totalPages, currentPage - 1, currentPage, currentPage + 1]);
        const ordered = [...pages]
            .filter(page => page >= 1 && page <= totalPages)
            .sort((left, right) => left - right);
        const result = [];

        for (let index = 0; index < ordered.length; index++) {
            const page = ordered[index];
            const previous = ordered[index - 1];
            if (index > 0 && page - previous > 1) {
                result.push('…');
            }
            result.push(page);
        }

        return result;
    }

    function pageButton(label, page, { active = false, disabled = false } = {}) {
        const button = document.createElement('button');
        button.type = 'button';
        button.className = `library-page-btn${active ? ' active' : ''}`;
        button.textContent = label;
        button.disabled = disabled;
        if (active) button.setAttribute('aria-current', 'page');
        button.addEventListener('click', () => {
            currentPage = page;
            render();
        });
        return button;
    }

    function renderPagination(totalPages) {
        elements.pagination.replaceChildren();
        elements.pagination.setAttribute('aria-label', tr('corrections_pages'));
        if (totalPages <= 1) return;

        elements.pagination.append(pageButton(tr('corrections_previous'), currentPage - 1, {
            disabled: currentPage === 1
        }));

        for (const entry of pageSequence(totalPages)) {
            if (entry === '…') {
                const gap = document.createElement('span');
                gap.className = 'corrections-page-gap';
                gap.textContent = '…';
                elements.pagination.append(gap);
                continue;
            }
            elements.pagination.append(pageButton(String(entry), entry, { active: entry === currentPage }));
        }

        elements.pagination.append(pageButton(tr('corrections_next'), currentPage + 1, {
            disabled: currentPage === totalPages
        }));
    }

    function coverElement(item) {
        const initialImageUrl = item.imageUrl || fallbackCoverUrl(item.appId);
        const hasImage = Boolean(initialImageUrl);
        const cover = document.createElement(hasImage ? 'img' : 'div');
        cover.className = 'correction-card-cover';
        cover.setAttribute('aria-hidden', 'true');

        if (hasImage) {
            cover.src = initialImageUrl;
            cover.alt = '';
            cover.loading = 'lazy';
            cover.decoding = 'async';
            cover.addEventListener('error', () => {
                const fallback = fallbackCoverUrl(item.appId);
                if (fallback && !cover.dataset.ryuuFallbackApplied && cover.src !== fallback) {
                    cover.dataset.ryuuFallbackApplied = 'true';
                    cover.src = fallback;
                    return;
                }
                const placeholder = document.createElement('div');
                placeholder.className = 'correction-card-cover correction-card-cover-placeholder';
                cover.replaceWith(placeholder);
            });
        } else {
            cover.classList.add('correction-card-cover-placeholder');
        }

        return cover;
    }

