    function resetProgressModal() {
        activeOperation = null;
        elements.progressModal.hidden = true;
        elements.progressCancel.disabled = false;
        const resolve = progressCloseResolver;
        progressCloseResolver = null;
        resolve?.();
        render();
    }

    function updateProgress(progress) {
        if (!activeOperation || progress.operationId !== activeOperation.operationId) return;

        if (!activeOperation.startedAt) activeOperation.startedAt = Date.now();
        const percent = Math.max(0, Math.min(100, Math.round(progress.percent || 0)));
        if (Number.isFinite(progress.transferredBytes)) {
            activeOperation.transferredBytes = Math.max(activeOperation.transferredBytes, progress.transferredBytes);
        }
        if (Number.isFinite(progress.totalBytes) && progress.totalBytes > 0) {
            activeOperation.totalBytes = progress.totalBytes;
        }
        if (Number.isFinite(progress.speedBytesPerSecond) && progress.speedBytesPerSecond > 0) {
            activeOperation.speedBytesPerSecond = progress.speedBytesPerSecond;
        }
        if (Number.isFinite(progress.remainingSeconds)) {
            activeOperation.remainingSeconds = progress.remainingSeconds;
        }

        elements.progressStage.textContent = stageLabel(progress.stage || 'preparing');
        elements.progressPercent.textContent = `${percent}%`;
        elements.progressBar.style.width = `${percent}%`;

        if (activeOperation.transferredBytes > 0) {
            elements.progressTransferred.textContent = activeOperation.totalBytes > 0
                ? `${formatBytes(activeOperation.transferredBytes)} / ${formatBytes(activeOperation.totalBytes)}`
                : formatBytes(activeOperation.transferredBytes);
        }
        elements.progressSpeed.textContent = activeOperation.speedBytesPerSecond
            ? `${formatBytes(activeOperation.speedBytesPerSecond)}/s`
            : '—';
        elements.progressRemaining.textContent = formatDuration(activeOperation.remainingSeconds);

        if (progress.stage && progress.stage !== 'downloading' && progress.stage !== 'preparing') {
            elements.progressCancel.hidden = true;
        }
    }

    function finalizeProgress(result, mode) {
        if (!activeOperation) return;
        activeOperation.result = result;
        elements.progressCancel.hidden = true;
        elements.progressClose.hidden = false;
        elements.progressClose.textContent = tr('corrections_progress_done');

        if (result.success) {
            const elapsedSeconds = activeOperation.startedAt
                ? Math.max(1, Math.round((Date.now() - activeOperation.startedAt) / 1000))
                : 1;
            const averageSpeed = activeOperation.speedBytesPerSecond;
            elements.progressCard.dataset.state = 'success';
            elements.progressTitle.textContent = tr(
                mode === 'download'
                    ? 'corrections_progress_download_success_title'
                    : 'corrections_progress_install_success_title'
            );
            elements.progressStage.textContent = stageLabel('completed');
            elements.progressPercent.textContent = '100%';
            elements.progressBar.style.width = '100%';
            elements.progressTransferred.textContent = activeOperation.totalBytes > 0
                ? `${formatBytes(activeOperation.totalBytes)} / ${formatBytes(activeOperation.totalBytes)}`
                : activeOperation.transferredBytes > 0
                    ? formatBytes(activeOperation.transferredBytes)
                    : tr('corrections_progress_downloaded_file');
            elements.progressSpeedLabel.textContent = tr('corrections_progress_average_speed');
            elements.progressSpeed.textContent = averageSpeed > 0 ? `${formatBytes(averageSpeed)}/s` : '—';
            elements.progressRemainingLabel.textContent = tr('corrections_progress_total_time');
            elements.progressRemaining.textContent = formatDuration(elapsedSeconds);
            elements.progressSuccessMessage.textContent = tr(
                mode === 'download' ? 'corrections_success_download' : 'corrections_success_install'
            );
            elements.progressSuccessMessage.hidden = false;

            if (mode === 'download') {
                elements.progressOpenFolder.hidden = false;
                elements.progressOpenFolder.dataset.folderPath = result.folderPath || '';
                notify(tr('corrections_success_download'), 'success');
            } else {
                elements.progressOpenFolder.hidden = true;
                notify(tr('corrections_success_install'), 'success');
            }
            return;
        }

        elements.progressCard.dataset.state = result.code === 'cancelled' ? 'cancelled' : 'error';
        elements.progressSuccessMessage.hidden = true;
        elements.progressOpenFolder.hidden = true;
        const key = result.code === 'cancelled'
            ? 'corrections_cancelled'
            : `corrections_error_${result.code || 'download_failed'}`;
        const translated = tr(key);
        const message = translated === key
            ? tr('corrections_error_download_failed')
            : translated;
        elements.progressStage.textContent = message;
        notify(message, result.code === 'cancelled' ? 'info' : 'error');
    }

    async function startDownload(item) {
        const operationId = `${Date.now()}-${Math.random().toString(16).slice(2)}`;
        showProgressModal(item, 'download', operationId);
        render();

        try {
            const result = await api.download({ appId: item.appId, operationId });
            finalizeProgress(result, 'download');
        } catch (_) {
            finalizeProgress({ success: false, code: 'download_failed' }, 'download');
        } finally {
            render();
        }
    }

    async function requestInstall(item) {
        const prepared = await api.prepareInstall(item.appId);
        if (!prepared.success) {
            const key = `corrections_error_${prepared.code || 'download_failed'}`;
            const translated = tr(key);
            notify(translated === key ? tr('corrections_error_download_failed') : translated, 'error');
            return;
        }
        openConfirmModal(item);
    }

    async function confirmInstall() {
        if (!pendingInstall) return;
        const item = pendingInstall;
        closeConfirmModal();

        await startInstall(item);
    }

    async function startInstall(item) {

        const operationId = `${Date.now()}-${Math.random().toString(16).slice(2)}`;
        showProgressModal(item, 'install', operationId);
        render();

        try {
            const result = await api.install({ appId: item.appId, operationId });
            finalizeProgress(result, 'install');
        } catch (_) {
            finalizeProgress({ success: false, code: 'apply_failed' }, 'install');
        } finally {
            render();
        }
    }

    async function runOfferDownload() {
        if (!pendingOffer) return;
        const offer = pendingOffer;
        elements.offerModal.hidden = true;
        const closed = waitForProgressClose();
        await startDownload(offer.item);
        await closed;
        if (pendingOffer === offer) finishOffer('download');
    }

