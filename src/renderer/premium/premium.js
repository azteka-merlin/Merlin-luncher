    function positionTooltip(anchor) {
        if (!anchor || elements.tooltip.hidden) return;
        const rect = anchor.getBoundingClientRect();
        const tooltipRect = elements.tooltip.getBoundingClientRect();
        const viewportPadding = 12;
        const preferredLeft = rect.right - tooltipRect.width;
        const preferredTop = rect.top + 10;
        const left = Math.min(
            window.innerWidth - tooltipRect.width - viewportPadding,
            Math.max(viewportPadding, preferredLeft)
        );
        const top = Math.min(
            window.innerHeight - tooltipRect.height - viewportPadding,
            Math.max(viewportPadding, preferredTop)
        );
        elements.tooltip.style.left = `${left}px`;
        elements.tooltip.style.top = `${top}px`;
    }

    function renderTooltip(item) {
        const cooldownEntries = getCooldownEntries(item);
        elements.tooltip.replaceChildren();

        const title = document.createElement('div');
        title.className = 'premium-tooltip-title';
        title.textContent = displayName(item);

        const meta = document.createElement('div');
        meta.className = 'premium-tooltip-meta';
        meta.innerHTML = `
            <p><strong>${tr('premium_mode_label')}:</strong> ${tr('premium_mode_value')}</p>
            <p><strong>${tr('premium_slots_label')}:</strong> ${item.availability.availableSlots}/${item.activationLimit}</p>
            <p><strong>${cooldownCountText(cooldownEntries.length)}</strong></p>
        `;

        const divider = document.createElement('div');
        divider.className = 'premium-tooltip-divider';

        const list = document.createElement('div');
        list.className = 'premium-tooltip-list';

        if (cooldownEntries.length === 0) {
            const empty = document.createElement('p');
            empty.className = 'premium-tooltip-empty';
            empty.textContent = tr('premium_available_now');
            list.append(empty);
        } else {
            cooldownEntries.forEach((entry, index) => {
                const line = document.createElement('p');
                line.textContent = tr('premium_user_release', {
                    index: index + 1,
                    date: formatDateTime(entry.availableAt)
                });
                list.append(line);
            });
        }

        elements.tooltip.append(title, meta, divider, list);
    }

