    function updateProgress(progress) {
        if (!activeOperation || progress.operationId !== activeOperation.operationId) return;
        if (!activeOperation.startedAt) activeOperation.startedAt = Date.now();

        const percent = Math.max(0, Math.min(100, Math.round(progress.percent || 0)));
        if (Number.isFinite(progress.transferredBytes)) {
            activeOperation.transferredBytes = Math.max(activeOperation.transferredBytes, progress.transferredBytes);
        }
        if (Number.isFinite(progress.totalBytes) && progress.totalBytes > 0) {
            activeOperation.totalBytes = progress.totalBytes;
        }
        if (Number.isFinite(progress.speedBytesPerSecond) && progress.speedBytesPerSecond > 0) {
            activeOperation.speedBytesPerSecond = progress.speedBytesPerSecond;
        }
        if (Number.isFinite(progress.remainingSeconds)) {
            activeOperation.remainingSeconds = progress.remainingSeconds;
        }

        elements.progressStage.textContent = stageLabel(progress.stage);
        elements.progressPercent.textContent = `${percent}%`;
        elements.progressBar.style.width = `${percent}%`;
        elements.progressTransferred.textContent = activeOperation.transferredBytes > 0
            ? activeOperation.totalBytes > 0
                ? `${formatBytes(activeOperation.transferredBytes)} / ${formatBytes(activeOperation.totalBytes)}`
                : formatBytes(activeOperation.transferredBytes)
            : '—';
        elements.progressSpeed.textContent = activeOperation.speedBytesPerSecond
            ? `${formatBytes(activeOperation.speedBytesPerSecond)}/s`
            : '—';
        elements.progressRemaining.textContent = formatDuration(activeOperation.remainingSeconds);

        if (progress.stage && progress.stage !== 'preparing' && progress.stage !== 'downloading') {
            elements.progressCancel.hidden = true;
        }
    }

    function finalizeProgress(result, item) {
        if (!activeOperation) return;
        activeOperation.result = result;
        elements.progressCancel.hidden = true;
        elements.progressClose.hidden = false;
        elements.progressClose.textContent = tr('premium_progress_done');

        if (result.success) {
            const elapsedSeconds = activeOperation.startedAt
                ? Math.max(1, Math.round((Date.now() - activeOperation.startedAt) / 1000))
                : 1;
            elements.progressCard.dataset.state = 'success';
            elements.progressTitle.textContent = tr('premium_progress_success_title');
            elements.progressStage.textContent = stageLabel('completed');
            elements.progressPercent.textContent = '100%';
            elements.progressBar.style.width = '100%';
            elements.progressTransferred.textContent = activeOperation.totalBytes > 0
                ? `${formatBytes(activeOperation.totalBytes)} / ${formatBytes(activeOperation.totalBytes)}`
                : tr('premium_progress_downloaded_file');
            elements.progressSpeedLabel.textContent = tr('premium_progress_average_speed');
            elements.progressSpeed.textContent = activeOperation.speedBytesPerSecond
                ? `${formatBytes(activeOperation.speedBytesPerSecond)}/s`
                : '—';
            elements.progressRemainingLabel.textContent = tr('premium_progress_total_time');
            elements.progressRemaining.textContent = formatDuration(elapsedSeconds);
            elements.progressSuccessMessage.textContent = tr('premium_success_activate');
            elements.progressSuccessMessage.hidden = false;
            renderLaunchNotice(result, item);
            elements.progressOpenFolder.hidden = false;
            elements.progressOpenFolder.dataset.appId = item.appId;
            notify(tr('premium_success_activate'), 'success');
            return;
        }

        elements.progressCard.dataset.state = result.code === 'cancelled' ? 'cancelled' : 'error';
        elements.progressSuccessMessage.hidden = true;
        elements.progressLaunchNotice.hidden = true;
        elements.progressLaunchNotice.replaceChildren();
        elements.progressOpenFolder.hidden = true;
        const key = `premium_error_${result.code || 'activate_failed'}`;
        const translated = tr(key);
        const message = translated === key ? tr('premium_error_activate_failed') : translated;
        elements.progressStage.textContent = message;
        notify(message, result.code === 'cancelled' ? 'info' : 'error');
    }

    async function activate(item) {
        if (activeOperation || loading) return;

        const operationId = `${Date.now()}-${Math.random().toString(16).slice(2)}`;
        showProgressModal(item, operationId);
        render();

        try {
            const result = await api.activate({ appId: item.appId, operationId });
            finalizeProgress(result, item);
            if (result.success) {
                await loadPremium(true);
            }
        } catch (_) {
            finalizeProgress({ success: false, code: 'activate_failed' }, item);
        } finally {
            render();
        }
    }

