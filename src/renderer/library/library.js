document.addEventListener('DOMContentLoaded', () => {
    const api = window.electronAPI.library;
    const pageSize = 10;
    const elements = {
        button: document.getElementById('libraryBtn'),
        view: document.getElementById('libraryView'),
        refresh: document.getElementById('libraryRefreshBtn'),
        refreshLabel: document.querySelector('#libraryRefreshBtn span'),
        search: document.getElementById('librarySearchInput'),
        loading: document.getElementById('libraryLoadingState'),
        error: document.getElementById('libraryErrorState'),
        empty: document.getElementById('libraryEmptyState'),
        noResults: document.getElementById('libraryNoResultsState'),
        tableWrap: document.getElementById('libraryTableWrap'),
        tableBody: document.getElementById('libraryTableBody'),
        footer: document.getElementById('libraryFooter'),
        resultCount: document.getElementById('libraryResultCount'),
        pagination: document.getElementById('libraryPagination'),
        retry: document.getElementById('libraryRetryBtn'),
        modal: document.getElementById('libraryRemoveModal'),
        modalMessage: document.getElementById('libraryModalGameMessage'),
        cancelRemove: document.getElementById('libraryCancelRemoveBtn'),
        confirmRemove: document.getElementById('libraryConfirmRemoveBtn')
    };

    let items = [];
    let loaded = false;
    let loading = false;
    let currentPage = 1;
    let pendingRemoval = null;

    function tr(key, values = {}) {
        const template = window.merlinI18n.t(key);
        return Object.entries(values).reduce(
            (text, [name, value]) => text.replaceAll(`{${name}}`, value),
            template
        );
    }

    function notify(message, type = 'info') {
        if (typeof window.showNotification === 'function') {
            window.showNotification(message, type);
        }
    }

    function displayName(item) {
        return item.gameName || tr('library_unknown_game', { appId: item.appId });
    }

    function fallbackCoverUrl(appId) {
        return /^\d+$/.test(String(appId || ''))
            ? `https://generator.ryuu.lol/files/images/${appId}.jpg`
            : null;
    }

    function createGameCellContent(item) {
        const wrap = document.createElement('div');
        wrap.className = 'library-game-cell';

        const cover = document.createElement(item.coverUrl ? 'img' : 'div');
        cover.className = 'library-game-cover';
        cover.setAttribute('aria-hidden', 'true');

        if (item.coverUrl) {
            cover.src = item.coverUrl;
            cover.alt = '';
            cover.loading = 'lazy';
            cover.decoding = 'async';
            cover.addEventListener('error', () => {
                const fallbackUrl = fallbackCoverUrl(item.appId);
                if (fallbackUrl && cover.src !== fallbackUrl) {
                    cover.src = fallbackUrl;
                    return;
                }
                const fallback = document.createElement('div');
                fallback.className = 'library-game-cover library-game-cover-placeholder';
                fallback.setAttribute('aria-hidden', 'true');
                cover.replaceWith(fallback);
            });
        } else {
            cover.classList.add('library-game-cover-placeholder');
        }

        const name = document.createElement('span');
        name.className = 'library-game-name';
        name.textContent = displayName(item);
        name.title = name.textContent;

        wrap.append(cover, name);
        return wrap;
    }

    function filteredItems() {
        return window.libraryModel.filterItems(items, elements.search.value);
    }

    function pageButton(label, page, options = {}) {
        const button = document.createElement('button');
        button.type = 'button';
        button.className = `library-page-btn${options.active ? ' active' : ''}`;
        button.textContent = label;
        button.disabled = Boolean(options.disabled);
        if (options.active) button.setAttribute('aria-current', 'page');
        button.addEventListener('click', () => {
            currentPage = page;
            render();
        });
        return button;
    }

    function renderPagination(totalPages) {
        elements.pagination.replaceChildren();
        elements.pagination.setAttribute('aria-label', tr('library_pages'));
        if (totalPages <= 1) return;
        elements.pagination.append(pageButton(tr('library_previous'), currentPage - 1, {
            disabled: currentPage === 1
        }));
        for (let page = 1; page <= totalPages; page++) {
            elements.pagination.append(pageButton(String(page), page, { active: page === currentPage }));
        }
        elements.pagination.append(pageButton(tr('library_next'), currentPage + 1, {
            disabled: currentPage === totalPages
        }));
    }

    async function exploreGameFiles(item, button) {
        button.disabled = true;
        try {
            const result = await api.openGameFolder(item.appId);
            if (result.success) return;
            const key = `library_error_${result.code || 'open_folder_failed'}`;
            const translated = tr(key);
            notify(translated === key ? tr('library_error_open_folder_failed') : translated, 'error');
        } catch (_) {
            notify(tr('library_error_open_folder_failed'), 'error');
        } finally {
            button.disabled = false;
        }
    }

    function render() {
        const filtered = filteredItems();
        const page = window.libraryModel.paginate(filtered, currentPage, pageSize);
        const { totalPages, visible } = page;
        currentPage = page.currentPage;

        elements.loading.hidden = true;
        elements.error.hidden = true;
        elements.empty.hidden = items.length !== 0;
        elements.noResults.hidden = items.length === 0 || filtered.length !== 0;
        elements.tableWrap.hidden = visible.length === 0;
        elements.footer.hidden = visible.length === 0;
        elements.tableBody.replaceChildren();

        for (const item of visible) {
            const row = document.createElement('tr');
            const gameCell = document.createElement('td');
            const gameName = displayName(item);
            gameCell.append(createGameCellContent(item));
            const appIdCell = document.createElement('td');
            appIdCell.textContent = item.appId;
            const actionCell = document.createElement('td');
            const actions = document.createElement('div');
            actions.className = 'library-row-actions';

            const exploreButton = document.createElement('button');
            exploreButton.type = 'button';
            exploreButton.className = 'library-explore-btn';
            exploreButton.title = tr('library_explore_tooltip');
            exploreButton.setAttribute('aria-label', `${tr('library_explore_tooltip')}: ${gameName}`);
            exploreButton.innerHTML = `
                <svg viewBox="0 0 24 24" aria-hidden="true">
                    <path d="M3.5 6.8A2.3 2.3 0 0 1 5.8 4.5h4l1.6 1.8h6.8a2.3 2.3 0 0 1 2.3 2.3v8.6a2.3 2.3 0 0 1-2.3 2.3H5.8a2.3 2.3 0 0 1-2.3-2.3V6.8Z"></path>
                </svg>`;
            exploreButton.addEventListener('click', () => exploreGameFiles(item, exploreButton));

            const removeButton = document.createElement('button');
            removeButton.type = 'button';
            removeButton.className = 'library-remove-btn';
            removeButton.title = tr('library_remove_tooltip');
            removeButton.setAttribute('aria-label', `${tr('library_remove_tooltip')}: ${gameName}`);
            removeButton.innerHTML = '<svg viewBox="0 0 24 24" aria-hidden="true"><path d="M3 6h18M8 6V4h8v2m-9 0 1 14h8l1-14M10 10v6m4-6v6"></path></svg>';
            removeButton.addEventListener('click', () => openRemoveModal(item));
            actions.append(exploreButton, removeButton);
            actionCell.append(actions);
            row.append(gameCell, appIdCell, actionCell);
            elements.tableBody.append(row);
        }

        elements.resultCount.textContent = tr('library_result_count', {
            current: visible.length,
            total: filtered.length
        });
        renderPagination(totalPages);
    }

    function showError(code) {
        elements.loading.hidden = true;
        elements.empty.hidden = true;
        elements.noResults.hidden = true;
        elements.tableWrap.hidden = true;
        elements.footer.hidden = true;
        elements.error.hidden = false;
        const key = `library_error_${code}`;
        const translated = tr(key);
        document.querySelector('#libraryErrorState p').textContent = translated === key
            ? tr('library_load_error')
            : translated;
    }

    async function loadLibrary(force = false) {
        if (loading) return;
        loading = true;
        elements.refresh.disabled = true;
        elements.refresh.classList.toggle('loading', force);
        elements.refreshLabel.textContent = tr(force ? 'library_refreshing' : 'library_refresh');
        if (!loaded) {
            elements.loading.hidden = false;
            elements.error.hidden = true;
        }

        try {
            const request = force ? api.refresh() : api.list();
            const minimumLoadingTime = force
                ? new Promise(resolve => setTimeout(resolve, 500))
                : Promise.resolve();
            const [result] = await Promise.all([request, minimumLoadingTime]);
            if (!result.success) {
                showError(result.code);
                return;
            }
            items = result.items;
            loaded = true;
            render();
        } catch (_) {
            showError('load_failed');
        } finally {
            loading = false;
            elements.refresh.disabled = false;
            elements.refresh.classList.remove('loading');
            elements.refreshLabel.textContent = tr('library_refresh');
        }
    }

    function syncVisibility() {
        const isActive = (window.merlinView?.get?.() || '') === 'library';
        elements.view.hidden = !isActive;
        elements.button.classList.toggle('active', isActive);
        elements.button.setAttribute('aria-pressed', String(isActive));
        if (!isActive) return;
        if (!loaded) loadLibrary();
    }

    function openRemoveModal(item) {
        pendingRemoval = item;
        elements.modalMessage.textContent = tr('library_remove_message', { name: displayName(item) });
        elements.modal.hidden = false;
        elements.confirmRemove.focus();
    }

    function closeRemoveModal() {
        elements.modal.hidden = true;
        pendingRemoval = null;
        elements.confirmRemove.disabled = false;
        elements.confirmRemove.textContent = tr('library_confirm_remove');
    }

    async function confirmRemoval() {
        if (!pendingRemoval) return;
        elements.confirmRemove.disabled = true;
        elements.cancelRemove.disabled = true;
        elements.confirmRemove.textContent = tr('library_removing');
        try {
            const result = await api.remove(pendingRemoval.appId);
            if (!result.success) {
                notify(tr('library_remove_error'), 'error');
                return;
            }
            closeRemoveModal();
            elements.cancelRemove.disabled = false;
            if (result.items) {
                items = result.items;
                render();
            }
            notify(tr('library_remove_success'), 'success');
            if (result.steamRunning && await window.merlinRestartPrompt.ask({
                titleKey: 'restart_prompt_title',
                title: window.merlinI18n.t('restart_prompt_title'),
                message: tr('library_restart_prompt'),
                cancelKey: 'restart_prompt_later',
                cancelLabel: window.merlinI18n.t('restart_prompt_later'),
                actionKey: 'restart_prompt_action',
                actionLabel: window.merlinI18n.t('restart_prompt_action')
            })) {
                const restart = await api.restartSteam();
                notify(tr(restart.success ? 'library_restart_success' : 'library_restart_error'),
                    restart.success ? 'success' : 'error');
            }
        } catch (_) {
            notify(tr('library_remove_error'), 'error');
        } finally {
            elements.confirmRemove.disabled = false;
            elements.cancelRemove.disabled = false;
            elements.confirmRemove.textContent = tr('library_confirm_remove');
            if (elements.modal.hidden) {
                requestAnimationFrame(() => elements.search.focus());
            }
        }
    }

    elements.button.addEventListener('click', () => window.merlinView?.set?.('library'));
    elements.refresh.addEventListener('click', () => loadLibrary(true));
    elements.retry.addEventListener('click', () => loadLibrary(true));
    elements.search.addEventListener('input', () => {
        currentPage = 1;
        render();
    });
    elements.cancelRemove.addEventListener('click', closeRemoveModal);
    elements.confirmRemove.addEventListener('click', confirmRemoval);
    elements.modal.addEventListener('click', event => {
        if (event.target === elements.modal) closeRemoveModal();
    });
    window.addEventListener('keydown', event => {
        if (event.key === 'Escape' && !elements.modal.hidden) closeRemoveModal();
    });
    window.addEventListener('merlin-view-changed', syncVisibility);
    window.addEventListener('merlin-language-changed', () => {
        if (pendingRemoval) {
            elements.modalMessage.textContent = tr('library_remove_message', {
                name: displayName(pendingRemoval)
            });
        }
        render();
    });
    api.onUpdated(updatedItems => {
        items = updatedItems;
        loaded = true;
        render();
    });
    syncVisibility();
});

